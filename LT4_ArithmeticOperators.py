#Arithmetic operators

x = 17
y = 7


#Addition

print("x + y is equal to",x+y)


#Subtraction

print("x - y is equal to",x-y)


#Multiplication

print("x * y is equal to",x*y)


#Division

print("x / y is equal to",x/y)


#Floor division

print("x // y is equal to",x//y)


#Modulus division

print("x % y is equal to",x%y)

#Exponent

print("x to the power 3 is equal to",x**3)


